struct filter {
  int32_t i1, i2;
};

static inline int32_t satadd32(int32_t a, int32_t b)
{
  if (a > 0 && b > INT32_MAX - a) return INT32_MAX;
  if (a < 0 && b < INT32_MIN - a) return INT32_MIN;
  return a + b;
}

// 8.24 fixed point
// Tick is 1/100 s = 167772
static inline int32_t filter_update(
  struct filter *f,
  int32_t th_a, int32_t dth_g
) {
  int32_t x1 = f->i2 - th_a;
  f->i1 = satadd32(f->i1, ((int64_t)x1 * 167772) >> 24);
  const int32_t omega = 3.0f;
  int32_t x2 =
    (((int64_t)f->i1 * 50331648) >> 24) + // omega
    (((int64_t)x1 * 71179699) >> 24); // sqrt(2) * omega
  int32_t x3 = dth_g - x2;
  f->i2 = satadd32(f->i2, ((int64_t)x3 * 167772) >> 24);
  return f->i2;
}

